# Minha solução
def área(larg, comp):
    calc = larg * comp
    print(f'A área de um terreno {larg:.1f}x{comp:.1f} é de {calc:.1f}m².')


# Programa Principal
print(' Controle de Terrenos ')
print('-' * 22)
área(float(input('LARGURA (m): ')), float(input('COMPRIMENTO (m): ')))

