cores = {
    'limpar': '\033[m',
    'verde': '\033[0;32m',
    'amarelo': '\033[0;33m',
    'azul': '\033[0;34m'
}
li = cores['limpar']
vd = cores['verde']
am = cores['amarelo']
az = cores['azul']

# Minha solução
listagem = ('Lápis', 1.75,
            'Borracha', 2,
            'Caderno', 15.90,
            'Estojo', 25,
            'Transferidor', 4.20,
            'Compasso', 9.99,
            'Mochila', 120.32,
            'Canetas', 22.30,
            'Livro', 34.90)
print(f'{az}-' * 40)
print(f'{am}{"LISTAGEM DE PREÇOS":^40}')
print(f'{az}-{li}' * 40)
for cont in range(0, len(listagem), 2):
    print(f'{listagem[cont]:.<30}{vd}R$ {listagem[cont-17]:>6.2f}{li}')
print(f'{az}-' * 40)

