def escreva(txt):
    tam = len(txt)
    print('~' * tam)
    print(f'{txt}')
    print(f'~' * tam)


# Programa Principal
escreva(' Gustavo Guanabara ')
escreva(' Curso de Python no YouTube ')
escreva(' CeV ')
escreva(' Olá, Mundo! ')

